#include "libft.h"

long	ft_atol(char *str)
{
	long	i[3];

	i[0] = 0;
	i[1] = 1;
	i[2] = 0;
	while (ft_iswhitespace(str[i[0]]))
		i[0]++;
	if (str[i[0]] == '-' || str[i[0]] == '+')
	{
		if (str[i[0]++] == '-')
			i[1] = -1;
	}
	while (str[i[0]] && ft_isdigit(str[i[0]]))
		i[2] = i[2] * 10 + str[i[0]++] - '0';
	return (i[2] * i[1]);
}
